#Faça um programa que peça dois números e imprima a soma.

#Definindo variaveis.
primeiro_valor = 0
segundo_valor = 0
resultado = 0

#Solicitando valores para o usuario.
primeiro_valor =int(input("Me informe o primeiro valor para somar: "))
segundo_valor =int(input("Me informe o segundo valor para somar: "))

#Realizando soma.
resultado = primeiro_valor + segundo_valor

#Exibindo resultado da soma.
print("resultado da soma:", resultado)
